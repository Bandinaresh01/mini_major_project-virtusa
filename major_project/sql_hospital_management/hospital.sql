-- 1. Most Consulted Doctors
SELECT d.Name, d.Specialization, COUNT(a.AppointmentID) AS ConsultationCount
FROM Doctors d
JOIN Appointments a ON d.DoctorID = a.DoctorID
GROUP BY d.Name, d.Specialization
ORDER BY ConsultationCount DESC;

-- 2. Monthly Revenue (Consultation + Treatment Costs for Completed apps)
SELECT TO_CHAR(a.AppointmentDate, 'YYYY-MM') AS Month,
       SUM(d.ConsultationFee + t.TreatmentCost) AS TotalRevenue
FROM Appointments a
JOIN Doctors d ON a.DoctorID = d.DoctorID
JOIN Treatments t ON a.TreatmentID = t.TreatmentID
WHERE a.Status = 'Completed'
GROUP BY TO_CHAR(a.AppointmentDate, 'YYYY-MM')
ORDER BY Month DESC;

-- 3. Common Diseases
SELECT t.DiseaseName, COUNT(a.AppointmentID) AS CaseCount
FROM Treatments t
JOIN Appointments a ON t.TreatmentID = a.TreatmentID
GROUP BY t.DiseaseName
ORDER BY CaseCount DESC;

-- 4. Patient Visit Frequency (Who comes to the hospital most?)
SELECT p.Name, COUNT(a.AppointmentID) AS TotalVisits
FROM Patients p
JOIN Appointments a ON p.PatientID = a.PatientID
GROUP BY p.Name
ORDER BY TotalVisits DESC;

-- 5. Doctor Performance / Revenue Generated By Doctor
SELECT d.Name, COUNT(a.AppointmentID) AS Appointments, SUM(d.ConsultationFee) AS FeeRevenue
FROM Doctors d
JOIN Appointments a ON d.DoctorID = a.DoctorID
WHERE a.Status = 'Completed'
GROUP BY d.Name
ORDER BY FeeRevenue DESC;
